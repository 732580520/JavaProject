package JavaScript.src.com.bjpowernode;

public class Demo {
    public static void main(String[] args) {
        // 打印10遍： 美女约吗?
        int x = 1;
        while (x <= 10) {
            System.out.println("美女约吗?" + x);
            x++;
        }

        System.out.println("x = " + x);

        System.out.println("====================");

        for(int i = 1; i <= 10; i++) {
            System.out.println("美女约吗?" + i);
        }
        // System.out.println("i = " + i);



    }
}
